#include <stdio.h>

int main()
{
	char karakter = 's';
	char *isim = "Serhat";
	int sayi = 23;
	float f = 33.3;
	double d = 44.4;
	
	printf("Karakter = %c\n", karakter);
	printf("Isim = %s\n", isim);
	printf("Integer = %d\n", sayi);
	printf("Float = %f\n", f);
	printf("Double = %lf\n", d);
	
	return 0;
	
}